const folderGamesContainer = document.getElementById("folder-games");
function prettyName(path) {
  const name = path.substring(path.lastIndexOf("/") + 1).replace(/\.[^.]+$/, "");
  const spaced = name.replace(/[_-]/g, " ").replace(/([a-z])([A-Z])/g, "$1 $2").replace(/([0-9])([A-Za-z])/g, "$1 $2").replace(/([A-Za-z])([0-9])/g, "$1 $2");
  return spaced.replace(/\b\w/g, c => c.toUpperCase());
}

if (folderGamesContainer) {
  const existing = new Set(Array.from(document.querySelectorAll("button[onclick]")).map(button => {
    const onclick = button.getAttribute("onclick");
    const match = onclick && onclick.match(/window\\.location\\.href=\\\'([^\\\']+)\\\'/);
    return match ? match[1] : null;
  }).filter(Boolean));
  folderGames.forEach(path => {
    if (existing.has(path)) return;
    const button = document.createElement("button");
    button.textContent = prettyName(path);
    button.onclick = () => window.location.href = path;
    folderGamesContainer.appendChild(button);
  });
  if (!folderGamesContainer.hasChildNodes()) {
    folderGamesContainer.textContent = "No new games were found.";
  }
}
